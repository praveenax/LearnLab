package com.kyrax.kingdom;

public class Kingdom {
    String name;
    int ecPower;
    int defPower;
    int offPower;
    int culPower;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getEcPower() {
        return ecPower;
    }

    public void setEcPower(int ecPower) {
        this.ecPower = ecPower;
    }

    public int getDefPower() {
        return defPower;
    }

    public void setDefPower(int defPower) {
        this.defPower = defPower;
    }

    public int getOffPower() {
        return offPower;
    }

    public void setOffPower(int offPower) {
        this.offPower = offPower;
    }

    public int getCulPower() {
        return culPower;
    }

    public void setCulPower(int culPower) {
        this.culPower = culPower;
    }
}
